// break and continue keywords

// break
for(let i=1;i<=10;i++)
{
    if(i===5)
    {
        break; // It will break the loop
    }
    console.log(i);
}

// continue
for(let j=1;j<=10;j++)
{
    if(j===5)
    {
        continue; // It will break the current itteration and will continue the loop
    }
    console.log(j);
}

