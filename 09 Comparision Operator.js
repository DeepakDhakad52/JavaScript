// == vs ===

num1 = "7";
num2 = 7;
 
console.log(num1==num2); //true (Checking only values not data type)
console.log(num1===num2); //flase (Checking both values and data type)

// != vs !==

console.log(num1!=num2); //false (Checking just values)

console.log(num1!==num2); //true (Checking both values and datatype)




