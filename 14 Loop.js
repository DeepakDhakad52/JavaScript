// DRY : Don't Repeat Yourself!


// While loop

let i=1;
while(i<=10)
{
    console.log(i++);
}


// for Loop

for(let j=1; j<=10;j++)
{
    console.log(j);
}

//do while loop

let k=1;
do{
    console.log(k++);
}while(k<=10);



// sum of n natural Number
let n=10;
total = (n*(n+1))/2; //formula
console.log(total);